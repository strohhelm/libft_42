#include "libft.h"

void	ft_putnbr_fd(long n, int fd)
{
	if (n < -2147483648 || n > 2147483647)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n %= 10 ;
	}
	if (n <= 10)
		n += '0';
	write(fd, &n, 1);
}
